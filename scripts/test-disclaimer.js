/*
 * test-disclaimer.js
 * Site-wide notice inserted directly under the menu/header on every page, explaining that this
 * is a demo site for auditing experiments. Self-contained (markup + styles) so it can be removed
 * by deleting this file and its <script> tag in head.html.
 */

const COPY = {
  title: 'Demo / test site',
  body: 'This site was built on Adobe Experience Manager (Edge Delivery Services) to evaluate '
    + 'Adobe Sites Optimizer (ASO), LLMO, and Semrush SEO audits. It was created end-to-end with '
    + 'Claude Code using Adobe connectors and skills, with imagery generated by Adobe Firefly. '
    + 'Content and images are illustrative and may not accurately represent real places, '
    + 'businesses, or events in Montréal.',
};

function injectStyles() {
  if (document.getElementById('test-disclaimer-styles')) return;
  const style = document.createElement('style');
  style.id = 'test-disclaimer-styles';
  style.textContent = `
    .test-disclaimer {
      margin: 0;
      padding: 12px 16px;
      background: #fff4e5;
      border-top: 1px solid #f0c987;
      border-bottom: 1px solid #f0c987;
      color: #5c3d00;
      font-size: 14px;
      line-height: 1.4;
    }
    .test-disclaimer > div {
      max-width: 1200px;
      margin: 0 auto;
      display: flex;
      gap: 10px;
      align-items: baseline;
    }
    .test-disclaimer .test-disclaimer-icon { font-size: 16px; line-height: 1.4; }
    .test-disclaimer strong { display: block; margin-bottom: 2px; }
  `;
  document.head.appendChild(style);
}

function buildBanner() {
  const aside = document.createElement('aside');
  aside.className = 'test-disclaimer';
  aside.setAttribute('role', 'note');
  aside.setAttribute('aria-label', 'Test site notice');
  const inner = document.createElement('div');
  const icon = document.createElement('span');
  icon.className = 'test-disclaimer-icon';
  icon.setAttribute('aria-hidden', 'true');
  icon.textContent = '🛠️';
  const text = document.createElement('p');
  text.style.margin = '0';
  const title = document.createElement('strong');
  title.textContent = COPY.title;
  text.appendChild(title);
  text.appendChild(document.createTextNode(COPY.body));
  inner.appendChild(icon);
  inner.appendChild(text);
  aside.appendChild(inner);
  return aside;
}

function insert() {
  if (document.querySelector('.test-disclaimer')) return;
  injectStyles();
  const banner = buildBanner();
  const header = document.querySelector('header');
  if (header) {
    header.insertAdjacentElement('afterend', banner);
  } else {
    document.body.prepend(banner);
  }
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', insert);
} else {
  insert();
}
